// Using of pop method
// Array.phototype.pop()
// the pop() method removes the last elements from an array and returns
// that elements. This method changes the length of the array.

const plants=['mango','broccoli','cauliflower','tomato','cabbage'];
console.log(plants); //[ 'mango', 'broccoli', 'cauliflower', 'tomato', 'cabbage' ]
console.log(plants.pop());// it remove last elements in an array.//cabbage
console.log(plants);//[ 'mango', 'broccoli', 'cauliflower', 'tomato' ]