// Array.phototype.shift()
// The shift() method removes the first element from an array and returns
// that removed elements. This method changes the length of the array

const plants=['mango','broccoli','cauliflower','tomato','cabbage'];
console.log(plants); //[ 'mango', 'broccoli', 'cauliflower', 'tomato', 'cabbage' ]
console.log(plants.shift());// it remove first elements in an array.//mango
console.log(plants);//[ 'broccoli', 'cauliflower', 'tomato', 'cabbage' ]